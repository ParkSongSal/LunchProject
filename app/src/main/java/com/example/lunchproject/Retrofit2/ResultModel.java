package com.example.lunchproject.Retrofit2;

public class ResultModel {
    int seq;
    String result;
    String userId;
    String menuKind;
    String menuName;
    String menuCode;
    String path;
    String Lasted_Date;
    String Nick_Name;
    String Student_Id;
    String reply_count;

    public int getSeq() {
        return seq;
    }

    public void setSeq(int seq) {
        this.seq = seq;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getMenuKind() {
        return menuKind;
    }

    public void setMenuKind(String menuKind) {
        this.menuKind = menuKind;
    }

    public String getMenuName() {
        return menuName;
    }

    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    public String getMenuCode() {
        return menuCode;
    }

    public void setMenuCode(String menuCode) {
        this.menuCode = menuCode;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getLasted_Date() {
        return Lasted_Date;
    }

    public void setLasted_Date(String lasted_Date) {
        Lasted_Date = lasted_Date;
    }

    public String getNick_Name() {
        return Nick_Name;
    }

    public void setNick_Name(String nick_Name) {
        Nick_Name = nick_Name;
    }

    public String getStudent_Id() {
        return Student_Id;
    }

    public void setStudent_Id(String student_Id) {
        Student_Id = student_Id;
    }

    public String getReply_count() {
        return reply_count;
    }

    public void setReply_count(String reply_count) {
        this.reply_count = reply_count;
    }
}
